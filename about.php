               <h3 class="text-uppercase"><b> About Twenty Four April</b></h3>
                <p>1135 lives.</p>
                <p>Gone. Shattered, crushed under the weight of untrammelled greed and the rivalry between two warring political parties.</p>
                <p>But the number, too numerous to comprehend, is still not final. As this website gets constructed on the first anniversaryof
                    the Rana Plaza disaster, we hear of the death of Abdus Sobhan in Pabna. His injuries had been too great. He finally succumbed.</p>
                <p>There are maimed and crippled others. More others as well, those whose injuries cannot be seen, who are persecuted by demons in their mind.
                    Salma, another garment factory worker, committed suicide this January. Her head hurt. It was unbearable, said her husband.</p>
                <p>“The struggle of man [sic] against power is the struggle of memory against forgetting.” These words of Milan Kundera are oft-cited
                    because they reach out in some way or the other to those who feel a deep unease at injustice.</p>
                <p>To struggle against power concretely, necessitates remembering prime minister Sheikh Hasina’s callous dismissal of the incident
                    in a CNN interview, “accidents happen.” It requires remembering that after 530 people had officially been declared dead, the
                    finance minister Abul Maal Abdul Muhith had said, it was not really “serious.” It means not forgetting that the government needed
                    to prove there was little support for the opposition BNP’s 36 hour countrywide strike on April 23-24, it needed to prove that
                    everything was normal, that factory wheels were running. Party henchmen had threatened workers, if they were absent for fear
                    of the large crack discovered on the third floor the previous day, there would be no pay. But then the building collapsed.
                    Who to blame? The home minister Dr Muhiuddin Khan Alamgir had famously told the BBC: it had collapsed because supporters of
                    the Bangladesh Nationalist Party had “shaken” the iron shuttered gate. It means bearing in mind that the BNP-Jamaat alliance,
                    members of the same ruling class as the AL, have produced more than their fair share of coverups and falsehoods, when in power.</p>
                <p>A deep sense of unease at injustice, whether at home or abroad because in this global world, what concerns me, concerns you as well.
                    Our lives are interrelated.</p>
                <p>Tick-tock, tick-tock, the Guardian’s interactive documentary, The Shirt on Your Back, released on Rana Plaza’s first anniversary,
                    sharply brings home the absurd disparities between those at the high, and those at the low end of the production-and-supply chain:
                    the clock totes up how long you have been on the website and compares the earnings of Mahmuda, a female worker, with the earnings
                    of UK retail sellers. If you have been on their website for say, 7:45 mts, Mahmuda’s earnings are £ 0.13 while the sales of
                    UK retailers is £ 569,000. The makers of the documentary tell us that Bangladesh is “a poor Asian country”, that, while it was
                    dubbed “the original ‘basket case’, a term coined by Henry Kissinger, the then US secretary of state,” it is less poorer now,
                    because of the garment industry. But they conveniently forget to mention that Kissinger is a war criminal, not only because
                    of his role in Chile, Vietnam, Cambodia and Laos, but also because of his support for the Pakistani military junta when it
                    unleashed a genocidal campaign on the civilians of Bangladesh in March 1971, the year Bangladesh won its independence.
                    But freedom was won only after the loss of many lives. To make sense of why so many people had to die, whether in 1971, or
                    in 2013, one needs to engage with history (not scavenge quotes). One needs to engage with power.</p>
                <p>This website is being launched with 365 posters, photographs of the posters were taken by Ayon Rehal, a student of Pathshala, and a member of twentyfourapril. The banner photograph of the website of Rana Plaza -- its pancaked floors spilling outward, lit by rescue work being undertaken at night -- was taken by award-winning photographer Taslima Akhter, a teacher of Pathshala, also a member of twentyfourapril.</p>
                <p>The work of archiving continues and we at twentyfourapril hope to upload the remaining few hundred posters, with victim names accessible on Google’s search engine, in the coming months.</p>
